import Foundation


class Car {
    var make: String
    var model: String
    var year: String
    var color: String
    var kms: String

    init(make: String, model: String, year: String, color: String, kms: String) {
        self.make = make
        self.model = model
        self.year = year
        self.color = color
        self.kms = kms
    }
}

